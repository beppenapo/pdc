  <h1>Cosa sono i cookies?</h1>
  <p>I cookies sono piccoli files di testo che i siti web usano per memorizzare e raccogliere informazioni attraverso il browser. Sono utilizzati principalmente per misurare e migliorare la qualità del  sito attraverso l'analisi del comportamento dei visitatori, per personalizzare le pagine e ricordare le preferenze degli utenti.</p>
  
  <p>La "cookie law" (Direttiva 2009/136/CE) ha modificato l'art. 122 del Codice in materia di protezione dei dati personali (D. Lgs. 196/2003): l'articolo richiede che sia ottenuto il consenso dai visitatori per memorizzare o raccogliere ogni informazione sul computer o su ogni altro dispositivo connesso al web. Diversamente, il regime precedente permetteva la memorizzazione e la raccolta di queste informazioni senza un consenso preventivo.</p>

  <p>Il Garante per la Protezione dei Dati Personali ha individuato le modalità semplificate per l'informativa e l'acquisizione del consenso per l'uso dei cookie nel provvedimento datato 8 maggio 2014 - doc. web n. 3118884 (Pubblicato sulla Gazzetta Ufficiale n. 126 del 3 giugno 2014) a cui queste note legali si conformano ai fini di maggiore trasparenza verso l'utente finale.</p>
